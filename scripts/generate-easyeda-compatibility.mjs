#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { format } from 'prettier';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, '..');
const sourcePath = resolve(repoRoot, 'config/easyeda-compatibility.json');
const outputPath = resolve(repoRoot, 'docs/reference/easyeda-compatibility.md');

function assert(condition, message) {
  if (!condition) throw new Error(`Invalid compatibility source: ${message}`);
}

function validateSource(source) {
  assert(source && typeof source === 'object', 'root must be an object');
  assert(source.schemaVersion === 1, 'schemaVersion must be 1');
  assert(/^\d{4}-\d{2}-\d{2}$/.test(source.lastReviewed), 'lastReviewed must be YYYY-MM-DD');
  assert(
    Number.isInteger(source.reviewPolicyDays) && source.reviewPolicyDays > 0,
    'reviewPolicyDays must be positive',
  );
  assert(Array.isArray(source.records) && source.records.length > 0, 'records must not be empty');

  const ids = new Set();
  for (const record of source.records) {
    assert(/^[a-z0-9-]+$/.test(record.id), `invalid record id ${record.id}`);
    assert(!ids.has(record.id), `duplicate record id ${record.id}`);
    ids.add(record.id);
    assert(record.status === 'live-validated', `${record.id} must use live-validated status`);
    assert(Number.isFinite(Date.parse(record.validatedAt)), `${record.id} validatedAt is invalid`);
    assert(
      Date.parse(record.reviewBy) > Date.parse(record.validatedAt),
      `${record.id} reviewBy must follow validation`,
    );
    const reviewWindowMs =
      Date.parse(`${record.reviewBy}T00:00:00.000Z`) -
      Date.parse(`${record.validatedAt.slice(0, 10)}T00:00:00.000Z`);
    assert(
      reviewWindowMs <= source.reviewPolicyDays * 24 * 60 * 60 * 1000,
      `${record.id} review window exceeds ${source.reviewPolicyDays} days`,
    );
    const today = new Date();
    const todayUtc = Date.UTC(today.getUTCFullYear(), today.getUTCMonth(), today.getUTCDate());
    assert(
      Date.parse(`${record.reviewBy}T23:59:59.999Z`) >= todayUtc,
      `${record.id} compatibility evidence expired on ${record.reviewBy}`,
    );
    assert(
      Array.isArray(record.capabilities) && record.capabilities.length > 0,
      `${record.id} needs capabilities`,
    );
    assert(
      Array.isArray(record.knownLimitations) && record.knownLimitations.length > 0,
      `${record.id} needs limitations`,
    );
    for (const capability of record.capabilities) {
      assert(
        ['live', 'fake-runtime', 'ci'].includes(capability.evidenceLevel),
        `${capability.id} has invalid evidence level`,
      );
      assert(
        ['passed', 'limited', 'blocked'].includes(capability.status),
        `${capability.id} has invalid status`,
      );
      assert(
        Array.isArray(capability.evidence) && capability.evidence.length > 0,
        `${capability.id} needs evidence`,
      );
    }
  }
}

function escapeCell(value) {
  return String(value)
    .replaceAll('|', String.raw`\|`)
    .replaceAll('\n', ' ');
}

function evidenceLabel(url) {
  const match = url.match(/\/(pull|issues)\/(\d+)$/);
  if (!match) return 'Evidence';
  return `${match[1] === 'pull' ? 'PR' : 'Issue'} #${match[2]}`;
}

function evidenceLinks(urls) {
  return urls.map((url) => `[${evidenceLabel(url)}](${url})`).join(', ');
}

function statusLabel(value) {
  return value === 'live-validated' ? 'Live validated' : value;
}

export async function renderCompatibilityMarkdown(source) {
  validateSource(source);
  const lines = [
    '<!-- Generated by scripts/generate-easyeda-compatibility.mjs. Do not edit directly. -->',
    '',
    '# EasyEDA Pro Compatibility',
    '',
    'This matrix records exact runtime evidence. Broad support ranges elsewhere in the repository are policy targets; only rows below are live-validation claims.',
    '',
    `- **Last reviewed:** ${source.lastReviewed}`,
    `- **Review policy:** refresh each live record within ${source.reviewPolicyDays} days or mark it stale/blocked.`,
    '',
    '## Evidence levels',
    '',
    '| Level | Meaning |',
    '| --- | --- |',
    '| `live` | Executed against the exact EasyEDA Pro desktop runtime and a disposable design fixture. |',
    '| `fake-runtime` | Executed through deterministic EasyEDA API/socket fixtures. No real EasyEDA compatibility claim is implied. |',
    '| `ci` | Cross-platform build, static analysis, packaging, or unit/integration evidence. |',
    '',
    '## Exact-version matrix',
    '',
    '| EasyEDA Pro | OS / architecture | MCP validation build | Released fix version | Extension package | Evidence status | Validated | Review by |',
    '| --- | --- | --- | --- | --- | --- | --- | --- |',
  ];

  for (const record of source.records) {
    lines.push(
      `| ${escapeCell(record.easyedaPro.version)} | ${escapeCell(record.environment.operatingSystem)} / ${escapeCell(record.environment.architecture)} | ${escapeCell(record.server.validationPackageVersion)} | ${escapeCell(record.server.releaseContainingFixes)} | ${escapeCell(record.extension.installedPackageVersion)} | ${statusLabel(record.status)} | ${record.validatedAt.slice(0, 10)} | ${record.reviewBy} |`,
    );
  }

  for (const record of source.records) {
    lines.push(
      '',
      `## ${record.easyedaPro.version} on ${record.environment.operatingSystem} (${record.environment.architecture})`,
      '',
      `Record ID: \`${record.id}\``,
      '',
      '### Runtime identity',
      '',
      '| Field | Exact value |',
      '| --- | --- |',
      `| EasyEDA Pro | \`${record.easyedaPro.version}\` |`,
      `| Electron | \`${record.easyedaPro.electronVersion}\` |`,
      `| Chromium | \`${record.easyedaPro.chromiumVersion}\` |`,
      `| Operating system | ${escapeCell(record.environment.operatingSystem)} |`,
      `| Kernel | \`${record.environment.kernel}\` |`,
      `| Architecture | \`${record.environment.architecture}\` |`,
      `| Node.js used by validation server | \`${record.environment.nodeVersion}\` |`,
      `| Validation package version | \`${record.server.validationPackageVersion}\` |`,
      `| Release containing validated fixes | \`${record.server.releaseContainingFixes}\` |`,
      `| Merge commit | \`${record.server.commit}\` |`,
      `| Installed extension package metadata | \`${record.extension.installedPackageVersion}\` |`,
      `| Loader-reported version | \`${record.extension.loaderReportedVersion}\` |`,
      `| Bridge contract | \`${record.extension.bridgeContractVersion}\` |`,
      `| Dispatcher | \`${record.extension.activeDispatcher}\` |`,
      `| Method registry hash | \`${record.extension.methodRegistryHash}\` |`,
      `| Hot-swap compiled / enabled | \`${record.extension.hotSwapCompiled}\` / \`${record.extension.hotSwapEnabled}\` |`,
      '',
      'The package metadata and loader-reported version are intentionally separate. The validation candidate reported loader `0.35.0`; the same production build was restored with extension package metadata `0.35.1`, and the fixes shipped in release `0.35.1`.',
      '',
      '### Capability evidence',
      '',
      '| Capability | Level | Status | Result | Evidence |',
      '| --- | --- | --- | --- | --- |',
    );

    for (const capability of record.capabilities) {
      const result = capability.limitation
        ? `${capability.summary} **Boundary:** ${capability.limitation}`
        : capability.summary;
      lines.push(
        `| ${escapeCell(capability.name)} | \`${capability.evidenceLevel}\` | \`${capability.status}\` | ${escapeCell(result)} | ${evidenceLinks(capability.evidence)} |`,
      );
    }

    lines.push('', '### Known limitations', '');
    for (const limitation of record.knownLimitations) lines.push(`- ${limitation}`);
  }

  lines.push(
    '',
    '## Reproducing live smoke evidence',
    '',
    'Normal CI does not require EasyEDA Pro. Live checks are opt-in and must use a disposable project:',
    '',
    '```bash',
    'EASYEDA_LIVE_TESTS=true \\',
    'EASYEDA_TEST_PROJECT_ID=<disposable-project-id> \\',
    'pnpm smoke:easyeda',
    '```',
    '',
    'Write checks require an additional explicit opt-in:',
    '',
    '```bash',
    'EASYEDA_LIVE_TESTS=true \\',
    'EASYEDA_TEST_PROJECT_ID=<disposable-project-id> \\',
    'EASYEDA_LIVE_WRITE_TESTS=true \\',
    'pnpm smoke:easyeda',
    '```',
    '',
    'The report must record the exact EasyEDA, extension, bridge, operating system, and server versions. A compatibility claim expires when its review date passes or when a safety-sensitive runtime change lands without fresh evidence.',
    '',
    '## Updating this matrix',
    '',
    'Edit `config/easyeda-compatibility.json`, then run:',
    '',
    '```bash',
    'pnpm generate:compatibility',
    'pnpm check:compatibility',
    '```',
    '',
  );

  return await format(`${lines.join('\n')}\n`, { parser: 'markdown', proseWrap: 'preserve' });
}

export function loadCompatibilitySource() {
  return JSON.parse(readFileSync(sourcePath, 'utf8'));
}

async function main() {
  const source = loadCompatibilitySource();
  const rendered = await renderCompatibilityMarkdown(source);
  if (process.argv.includes('--check')) {
    const existing = readFileSync(outputPath, 'utf8').replaceAll('\r\n', '\n');
    if (existing !== rendered) {
      console.error(
        'EasyEDA compatibility documentation is stale. Run pnpm generate:compatibility.',
      );
      process.exitCode = 1;
      return;
    }
    console.log('EasyEDA compatibility documentation is current.');
    return;
  }
  writeFileSync(outputPath, rendered);
  console.log(`Generated ${outputPath}`);
}

if (process.argv[1] && import.meta.url === pathToFileURL(resolve(process.argv[1])).href) {
  try {
    await main();
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
